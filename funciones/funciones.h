int sumar(int a, int b);
int restar(int a, int b);
int mul(int a, int b);
int div(int a, int b);

int sumar(int a, int b){
	int res = a + b;
	return res;
}

int restar(int a, int b){
	int res = a - b;
	return res;
}

int mul(int a, int b){
	int res = a * b;
	return res;
}

int div(int a, int b){
	int res = a / b;
	return res;
}

